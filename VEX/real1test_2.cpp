#define PIDC 5
#define DEADBAND 100
#define PID_TASK_DELAY 30
#define MIN_LIFT 200

float kp[10],ki[10],kd[10];
int lastE[10];
float lastI[10];
int target[10];
int pido[10];
int rpos[10];
int lim[10];

void updatePID(){
	int tdiff = time1[T1];

	if(tdiff>=DEADBAND){
		for(int ch = 0; ch < PIDC; ch++){
			float ci, cd, sum;
			int et;
			//calculate error
			et = target[ch]-rpos[ch];
			// "i" and "d"
			cd = (et-lastE[ch])/(float)tdiff;
			// calculate I term + something that I forgot the name lol
			if(-150<et&&et<150&&et!=0){
				ci = lastI[ch] + (lastE[ch]+et)/2.0*tdiff;
			}else{
				ci = 0;
			}

			lastE[ch] = et;
			lastI[ch] = ci;
			sum = kp[ch]*et + ki[ch]*ci + kd[ch]*cd;

			pido[ch] = bound((int)sum,-lim[ch],lim[ch]);
		}
		clearTimer(T1);
	}
}

