void pickMobile(){
	if(isMobileOut){
		while(nMotorEncoder[rmobile]<700&&!vexRT[Btn7L]){
			motorReq[rmobile] = -128;
			motorReq[lmobile] = -128;
		}
		motorReq[rmobile] = 0;
		motorReq[lmobile] = 0;
	}else{
		while(nMotorEncoder[rmobile]>50&&!vexRT[Btn7L]){
			motorReq[rmobile] = 128;
			motorReq[lmobile] = 128;
		}
		motorReq[rmobile] = 0;
		motorReq[lmobile] = 0;
	}
	isMobileOut = !isMobileOut;
}

void pickCone(){
	if(isFlipRect){
		while(nMotorEncoder[flip]<430&&!vexRT[Btn7L]){
			motorReq[flip] = 128;
		}
		motorReq[flip] = 0;
		motorReq[claw] = -80;
	}else{
		while(nMotorEncoder[flip]>40&&!vexRT[Btn7L]){
			motorReq[flip] = -128;
		}
		motorReq[flip] = 0;
		motorReq[claw] = 80;
		wait(0.2);
		motorReq[claw] = 0;
	}
	isFlipRect = !isFlipRect;
}

void liftUp(){
	int newPos = liftOrigin+time1[T2]/3;
	newPos = bound(newPos, 200, 1000);
	target[0] = newPos;
	target[1] = newPos;
}

void liftDown(){
	int newPos = liftOrigin-time1[T2]/3;
	newPos = bound(newPos, 200, 1000);
	target[0] = newPos;
	target[1] = newPos;
}

